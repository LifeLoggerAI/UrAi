export function getPassiveSourceDefinition(id: string): PassiveDataSourceDefinition | undefined {
  return URAI_PASSIVE_SOURCES.find((source) => source.id === id);
}

export function getPassiveSourcesByPassportLayer(layerId: string): PassiveDataSourceDefinition[] {
  return URAI_PASSIVE_SOURCES.filter((source) => source.passportLayerId === layerId);
}

export function getDefaultPassiveSourceStatuses(): PassiveSourceStatus[] {
  return URAI_PASSIVE_SOURCES.map((source) => ({
    sourceId: source.id,
    status: source.defaultEnabled ? "ready" : "disabled",
    enabled: source.defaultEnabled,
    paused: false,
    localOnly: !source.supportsCloudSync,
    cloudSyncEnabled: false,
    rawDataOptIn: false,
    lastUpdatedAt: new Date().toISOString(),
  }));
}
