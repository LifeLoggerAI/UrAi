#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

const outputDir = process.env.WEBXR_AUDIT_OUTPUT_DIR || "/tmp/urai-webxr-audit-summary";
const summaryPath = path.join(outputDir, "webxr-audit-summary.md");
const jsonPath = path.join(outputDir, "webxr-audit-summary.json");

const commitSha = process.env.GITHUB_SHA || "local";
const repo = process.env.GITHUB_REPOSITORY || "LifeLoggerAI/UrAi";
const runId = process.env.GITHUB_RUN_ID || "local";
const serverUrl = process.env.GITHUB_SERVER_URL || "https://github.com";
const runUrl = runId === "local" ? "local" : `${serverUrl}/${repo}/actions/runs/${runId}`;
const generatedAt = new Date().toISOString();

const status = {
  repo,
  commitSha,
  runId,
  runUrl,
  generatedAt,
  status: "WEBXR FOUNDATION AUDIT ARTIFACT GENERATED",
  currentLaunchLabel: "HOME QUEST/XR DEPLOY-CONFIGURED",
  blockedUntilLiveEvidence: [
    "HOME QUEST/XR LIVE-SMOKE-PASSED",
    "HOME QUEST/XR LIVE-QUEST-VERIFIED",
    "Quest ready",
    "full VR ready",
    "full XR live",
  ],
  proofIssue: "https://github.com/LifeLoggerAI/UrAi/issues/348",
  deployWorkflow: "Deploy Home XR Firebase Hosting",
  auditWorkflow: "WebXR Foundation Audit",
};

fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(jsonPath, `${JSON.stringify(status, null, 2)}\n`);

fs.writeFileSync(
  summaryPath,
  `# WebXR Foundation Audit Summary\n\n` +
    `Generated: ${generatedAt}\n\n` +
    `Repository: ${repo}\n\n` +
    `Commit: ${commitSha}\n\n` +
    `Workflow run: ${runUrl}\n\n` +
    `Tracking issue: https://github.com/LifeLoggerAI/UrAi/issues/348\n\n` +
    `## Current launch label\n\n` +
    `HOME QUEST/XR DEPLOY-CONFIGURED\n\n` +
    `## What this artifact proves\n\n` +
    `This artifact is generated by the non-deploying WebXR Foundation Audit workflow. It proves the repository-side audit workflow ran far enough to create a summary artifact. It does not prove Firebase deployment or physical Quest hardware validation.\n\n` +
    `## Claims still blocked until live evidence exists\n\n` +
    `- HOME QUEST/XR LIVE-SMOKE-PASSED\n` +
    `- HOME QUEST/XR LIVE-QUEST-VERIFIED\n` +
    `- Quest ready\n` +
    `- full VR ready\n` +
    `- full XR live\n\n` +
    `## Required next proof\n\n` +
    `1. Run Deploy Home XR Firebase Hosting.\n` +
    `2. Attach deploy artifact home-xr-deploy-proof.\n` +
    `3. Record live URL, deployed commit, deploy run URL, route HTTP 200 checks, screenshots, and Quest hardware proof in issue #348 and HOME_XR_LIVE_DEPLOY_VERIFICATION.md.\n`,
);

console.log(`WebXR audit summary written to ${summaryPath}`);
console.log(`WebXR audit JSON written to ${jsonPath}`);
